public class JTextFrenchEnglish
{
	String fr;
	String en;

	public JTextFrenchEnglish(String pfr, String pen)
	{
		super();
		this.fr = pfr;
		this.en = pen;
	}

	public String getFr()
	{
		return this.fr;
	}

	public String getEn()
	{
		return this.en;
	}

}
